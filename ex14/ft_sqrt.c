int	ft_sqrt(int nb)
{
	int	test;

	test = 0;
	if (nb <= 0)
		return (0);
	if (nb == 1)
		return (1);
	while (test < nb)
	{
		if (test * test == nb)
			return (test);
		test++;
	}
	return (0);
}
